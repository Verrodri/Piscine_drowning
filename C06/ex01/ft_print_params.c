#include <unistd.h>

int	main(int ac, char **av)
{
	int	a;
	int	b;

	(void)ac;
	a = 1;
	while (av[a])
	{
		b = 0;
		while (av[a][b])
		{
			write(1, &av[a][b], 1);
			b++;
		}
		a++;
		write(1, "\n", 1);
	}
}
